@dataclass
class Island:
    """An island with its own CVT-MAP-Elites archive."""

    index: int
    pool: CVTMAPElitesPool
    best_score: float = 0.0
    best_program: Optional[Program] = None
    eval_count: int = 0
    acceptance_count: int = 0

    def get_top_elites(self, fraction: float = 0.1) -> list[tuple[Program, EvaluationResult, FeatureVector]]:
        """Get top fraction of elites by score."""
        elites = list(self.pool._elites.values())
        if not elites:
            return []
        elites.sort(key=lambda e: e.result.primary_score, reverse=True)
        n_top = max(1, int(len(elites) * fraction))
        return [(e.program, e.result, e.behavior) for e in elites[:n_top]]

    def receive_migrants(self, migrants: list[tuple[Program, EvaluationResult]]) -> int:
        """Receive migrants from another island. Returns count of accepted."""
        accepted = 0
        for program, result in migrants:
            if self.pool.add(program, result):
                accepted += 1
                if result.primary_score > self.best_score:
                    self.best_score = result.primary_score
                    self.best_program = program
        return accepted


