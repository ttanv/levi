def save_multi_island_state(
    generation: int,
    islands: list[Island],
    global_best_score: float,
    global_best_program: Program,
    total_cost: float,
    extra_info: dict = None,
    filename: Path = None,
):
    """Save multi-island evolution state to JSON file."""
    island_data = []
    for island in islands:
        elites = []
        try:
            for cell_idx, elite in island.pool._elites.items():
                elite_info = {
                    "cell_index": int(cell_idx),
                    "score": float(elite.result.primary_score),
                    "code": elite.program.code,
                }
                elites.append(elite_info)
        except AttributeError:
            pass
        elites.sort(key=lambda x: x["score"], reverse=True)

        island_data.append({
            "index": island.index,
            "archive_size": island.pool.size(),
            "best_score": float(island.best_score),
            "eval_count": island.eval_count,
            "acceptance_count": island.acceptance_count,
            "top_elites": elites[:5],  # Top 5 per island
        })

    state = {
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
        "generation": generation,
        "n_islands": len(islands),
        "global_best_score": float(global_best_score),
        "total_cost_usd": float(total_cost),
        "global_best_program": {
            "code": global_best_program.code,
            "metadata": {k: float(v) if isinstance(v, (int, float)) else str(v)
                        for k, v in (global_best_program.metadata or {}).items()},
        },
        "islands": island_data,
    }

    if extra_info:
        state.update(extra_info)

    target_file = filename if filename else STATE_FILE
    temp_file = target_file.with_suffix(".json.tmp")
    with open(temp_file, "w") as f:
        json.dump(state, f, indent=2)
    temp_file.rename(target_file)


async def generate_with_thinking(prompt: str, model: str, temperature: float = 0.7) -> dict:
    """Generate code using a model with thinking/reasoning enabled."""
    start = time.time()
    try:
        call_kwargs = {
            "model": model,
            "messages": [{"role": "user", "content": prompt}],
            "temperature": temperature,
            "max_tokens": 30000,
            "timeout": 300,
        }

        # Enable reasoning for DeepSeek models
        if "deepseek" in model.lower():
            call_kwargs["reasoning"] = {"enabled": True}

        response = await litellm.acompletion(**call_kwargs)
        elapsed = time.time() - start
        content = response.choices[0].message.content
        cost = litellm.completion_cost(completion_response=response)
        tokens = response.usage.total_tokens

        return {
            "content": content,
            "cost": cost,
            "tokens": tokens,
            "elapsed": elapsed,
            "model": model,
        }
    except Exception as e:
        elapsed = time.time() - start
        return {"error": str(e), "elapsed": elapsed}


async def generate_for_island(island_idx: int, prompt: str, model: str, temperature: float) -> dict:
    """Generate code for one island asynchronously."""
    start = time.time()
    try:
        response = await litellm.acompletion(
            model=model,
            messages=[{"role": "user", "content": prompt}],
            temperature=temperature,
            max_tokens=30000,
            timeout=300,
        )
        elapsed = time.time() - start
        content = response.choices[0].message.content
        cost = litellm.completion_cost(completion_response=response)
        tokens = response.usage.total_tokens
        print(f"  [LLM] Island {island_idx} done in {elapsed:.1f}s, {tokens} tokens")
        return {
            "island": island_idx,
            "content": content,
            "cost": cost,
            "tokens": tokens,
            "model": model,
        }
    except Exception as e:
        elapsed = time.time() - start
        print(f"  [LLM] Island {island_idx} FAILED after {elapsed:.1f}s: {e}")
        return {"island": island_idx, "error": str(e)}


