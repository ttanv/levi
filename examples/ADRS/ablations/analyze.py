"""
Ablation Analysis: Generates plots and summary tables.

Reads snapshot.json files from runs/ablations/<ablation>/<condition>_seed<N>/
and produces:
  - Best-so-far curves (mean ± std over seeds)
  - Final archive size bars
  - Summary table (mean±std score, AUC, time-to-threshold, archive size)
  - CSV export

Usage:
  python analyze.py a1                # Archive type ablation
  python analyze.py b1                # Init noise ablation
  python analyze.py d1                # Parallelism ablation
  python analyze.py all               # All ablations
  python analyze.py a1 --vs-time      # Plot vs wall-clock seconds (also generated by default)
"""

